using EventSystem.Models;
using UnityEngine;
using UnityEngine.Audio;

namespace EventSystem.VisualEditor.Nodes.Audio
{
    /// <summary>
    /// DO NOT PUT ANY CODE HERE, WITH THE EXCEPTION OF EDITOR CODE
    /// </summary>
    [NodeTint("#577590")]
    public class StartAudioNode : SkippableBaseNode
    {
        [Input] public NodeLink entry;
        [Output] public NodeLink exit;

        [Tooltip("Documentation purposes only"), TextArea]
        public string description;

        [Tooltip("Source of audio, not required")]
        public GameObject audioSourceLocation; 
        
        public AudioClip audioClip;

        [Tooltip("The target group to which the AudioSource should route its signal.")]
        public AudioMixerGroup audioMixer;
        
        [Tooltip("Un- / Mutes the AudioSource. Mute sets the volume=0, Un-Mute restore the original volume.")]
        public bool mute;
        
        [Tooltip("By making the Audio 'public' this will allow other audio nodes to access this audio source")]
        public bool isPublic;

        [Tooltip("Id that will be used to reference from other nodes")]
        public string publicId;
        
        [Header("Audio control")]
        
        [Tooltip("Bypass effects (Applied from filter components or global listener filters).")]
        public bool bypassEffects;
        
        [Tooltip("When set global effects on the AudioListener will not be applied to the audio signal generated by the AudioSource. Does not apply if the AudioSource is playing into a mixer group.")]
        public bool bypassListenerEffects;
        
        [Tooltip("When set doesn't route the signal from an AudioSource into the global reverb associated with reverb zones.")]
        public bool bypassReverbZones;
        
        [Tooltip("Is the audio clip looping?")]
        public bool loop;

        [Range(0, 256), Tooltip("Sets the priority of the AudioSource.")]
        public int priority = 128;

        [Range(0, 1), Tooltip("The volume of the audio source (0.0 to 1.0).")]
        public float volume = 1;

        [Range(-3, 3), Tooltip("The volume of the audio source (0.0 to 1.0).")]
        public float pitch = 1;
        
        [Range(-1,1), Tooltip("Pans a playing sound in a stereo way (left or right). This only applies to sounds that are Mono or Stereo.")]
        public float stereoPan;

        [Range(0, 1), Tooltip("Sets how much this AudioSource is affected by 3D spatialisation calculations (attenuation, doppler etc). 0.0 makes the sound full 2D, 1.0 makes it full 3D.")]
        public float spatialBlend;

        [Range(0, 1.1f), Tooltip("The amount by which the signal from the AudioSource will be mixed into the global reverb associated with the Reverb Zones.")]
        public float reverbZoneMix = 1;

        [Header("Audio Effects")]
        [Tooltip("Fades the audio source in/out over the specified properties")]
        public bool audioFade;
        
        [Tooltip("Audio level that the volume will end at when fade has completed")]
        public float targetVolume;
        
        [Tooltip("Length of time that the fade is occuring for")]
        public float fadeDuration;
        
        [Tooltip("Length of time before the fade begins on the audio source")]
        public float initialFadeDelay;
    }
}